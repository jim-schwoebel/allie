import os
import soundfile as sf

def clean_towav(audiofile):
	if audiofile.endswith('.mp3') or audiofile.endswith('.mp4'):
		os.system('ffmpeg -i %s %s'%(audiofile, audiofile[0:-4]+'.wav'))
		os.remove(audiofile)
